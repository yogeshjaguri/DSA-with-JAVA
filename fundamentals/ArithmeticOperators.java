public class ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic Operators

        // Addition
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);

        // Subtraction
        int d = 10;
        int e = 20;
        int f = d - e;
        System.out.println(f);

        // Multiplication
        int g = 10;
        int h = 20;
        int i = g * h;
        System.out.println(i);

        // Division
        int j = 10;
        int k = 20;
        int l = k / j;
        System.out.println(l);

        // Modulus
        int m = 10;
        int n = 20;
        int o = n % m;
        System.out.println(o);

        // Increment
        int p = 10;
        p++;
        System.out.println(p);

        // Decrement
        int q = 10;
        q--;
        System.out.println(q);

        // Compound Assignment
        int r = 10;
        r += 20;
        System.out.println(r);

        // Compound Assignment
        int s = 10;
        s -= 20;
        System.out.println(s);

        // Compound Assignment
        int t = 10;
        t *= 20;
        System.out.println(t);

        // Compound Assignment
        int u = 10;
        u /= 20;
        System.out.println(u);

        // Compound Assignment
        int v = 10;
        v %= 20;
        System.out.println(v);

        // Compound Assignment
        int w = 10;
        w &= 20;
        System.out.println(w);

        // Compound Assignment
        int x = 10;
        x |= 20;
        System.out.println(x);

        // Compound Assignment
        int y = 10;
        y ^= 20;
        System.out.println(y);

        // Compound Assignment
        int z = 10;
        z >>= 20;
        System.out.println(z);

        // Compound Assignment
        int aa = 10;
        aa <<= 20;
        System.out.println(aa);

        // Compound Assignment
        int ab = 10;
        ab >>>= 20;
        System.out.println(ab);

        // operator precedence ==> division has higher precedence than addition
        // multiplication has higher precedence than addition
        // parenthesis has higher precedence than addition
        // precidence order : () > / > * > + > -

        // power of a number is not a operator in java

    }
}
